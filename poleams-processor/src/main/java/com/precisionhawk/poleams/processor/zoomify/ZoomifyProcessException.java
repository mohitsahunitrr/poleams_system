public class ZoomifyProcessException extends Exception {

    public ZoomifyProcessException() {
    }

    public ZoomifyProcessException(String message) {
        super(message);
    }

    public ZoomifyProcessException(String message, Throwable cause) {
        super(message, cause);
    }

    public ZoomifyProcessException(Throwable cause) {
        super(cause);
    }
    
}
